void main() {
  // null digunakan sebagai ganti dari sebuah variabel yang tidak memiliki nilai.
  // masing masing variabel harus memiliki nilai untuk di deklarasikan terlebih dahulu
  // namun jika ingin mendkalarasikan variabel tsb tanpa harus memiliki nilai, maka null adalah solusinya

  int number1;
  number1 = 10;

  int? number2;
  number2 = null;

  print(number1);
  print(number2);
}
